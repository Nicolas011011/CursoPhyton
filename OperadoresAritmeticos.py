numero = 10
numero2 = 12

##suma
suma = numero+numero2
print (suma)

##resta
resta = numero-numero2
print (resta)

##mult
multiplicacion = numero*numero2
print (multiplicacion)

##division
division = numero/numero2
print (division)

##residuo
residuo = numero%numero2
print (residuo)

